export function generateHeader(projectName: string, author: string): string {
  const lines = [
    '-- ============================================================',
    `-- ${projectName}`,
  ];
  if (author) lines.push(`-- Author: ${author}`);
  lines.push(`-- Generated by CCraft Studio`);
  lines.push(`-- Date: ${new Date().toISOString().split('T')[0]}`);
  lines.push('-- ============================================================');
  lines.push('');
  return lines.join('\n');
}
