import { escapeLuaString } from '../../utils/luaHelpers';

export function generateEventLoopTemplate(screenNames: string[], startScreen: string): string {
  return `
-- ============================================================
-- Runtime Engine
-- ============================================================

local currentScreen = "${escapeLuaString(startScreen)}"
local running = true

local function navigate(screenName)
  currentScreen = screenName
  refreshScreen()
  if handlers[currentScreen] and handlers[currentScreen].onLoad then
    handlers[currentScreen].onLoad()
  end
end

local function refreshScreen()
  local drawFn = screenDrawFunctions[currentScreen]
  if drawFn then drawFn() end
end

-- Initialize elements state
local elements = {}
${screenNames.map(name => `elements["${escapeLuaString(name)}"] = elements["${escapeLuaString(name)}"] or {}`).join('\n')}

-- Screen draw function map
local screenDrawFunctions = {
${screenNames.map(name => `  ["${escapeLuaString(name)}"] = drawScreen_${name.replace(/[^a-zA-Z0-9_]/g, '_')},`).join('\n')}
}

-- Event handlers map
local handlers = {}

-- Draw initial screen
refreshScreen()
if handlers[currentScreen] and handlers[currentScreen].onLoad then
  handlers[currentScreen].onLoad()
end

-- Main event loop
while running do
  local event, p1, p2, p3, p4, p5 = os.pullEvent()

  if event == "mouse_click" or event == "monitor_touch" then
    local button, mx, my = p1, p2, p3
    if event == "monitor_touch" then
      mx, my = p1, p2
    end
    -- Check button clicks
    if handlers[currentScreen] and handlers[currentScreen].onButtonClick then
      for name, handler in pairs(handlers[currentScreen].onButtonClick) do
        local el = elements[name]
        if el and el.visible ~= false then
          if mx >= el.x and mx < el.x + el.width and my >= el.y and my < el.y + el.height then
            handler(mx, my, button)
          end
        end
      end
    end

  elseif event == "key" then
    local key = p1
    if handlers[currentScreen] and handlers[currentScreen].onKeyPress then
      for keyName, handler in pairs(handlers[currentScreen].onKeyPress) do
        if keys.getName(key) == keyName or keyName == "any" then
          handler(key)
        end
      end
    end

  elseif event == "timer" then
    if handlers[currentScreen] and handlers[currentScreen].onTimer then
      for _, handler in pairs(handlers[currentScreen].onTimer) do
        handler(p1)
      end
    end

  elseif event == "redstone" then
    if handlers[currentScreen] and handlers[currentScreen].onRedstone then
      handlers[currentScreen].onRedstone()
    end

  elseif event == "modem_message" then
    if handlers[currentScreen] and handlers[currentScreen].onModemMessage then
      for _, handler in pairs(handlers[currentScreen].onModemMessage) do
        handler(p1, p2, p3, p4, p5)
      end
    end
  end
end
`.trim();
}

export function generateHeader(projectName: string, author: string): string {
  const lines = [
    '-- ============================================================',
    `-- ${projectName}`,
  ];
  if (author) lines.push(`-- Author: ${author}`);
  lines.push(`-- Generated by CCraft IDE`);
  lines.push(`-- Date: ${new Date().toISOString().split('T')[0]}`);
  lines.push('-- ============================================================');
  lines.push('');
  return lines.join('\n');
}

export function generateElementsInit(screenName: string, elements: { name: string; type: string; x: number; y: number; width: number; height: number; visible: boolean; [key: string]: any }[]): string {
  const lines: string[] = [];
  lines.push(`-- Element state for screen: ${screenName}`);
  for (const el of elements) {
    const escaped = el.name.replace(/[^a-zA-Z0-9_]/g, '_');
    lines.push(`elements["${escapeLuaString(el.name)}"] = {`);
    lines.push(`  x = ${el.x}, y = ${el.y}, width = ${el.width}, height = ${el.height},`);
    lines.push(`  visible = ${el.visible},`);
    if (el.text !== undefined) {
      lines.push(`  text = "${escapeLuaString(el.text)}",`);
    }
    lines.push(`}`);
  }
  return lines.join('\n');
}
